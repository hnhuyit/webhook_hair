async function updateLastInteractionOnlyIfNewDay(userId, event_name) {
  try {
    const records = await base(TABLE_NAME)
      .select({
        filterByFormula: `{ZaloUID} = '${userId}'`,
        maxRecords: 1,
      })
      .firstPage();

    const todayISOString = new Date().toISOString();

    if (records.length === 0) {
      console.warn("‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y user ‚Üí ti·∫øn h√†nh t·∫°o m·ªõi:", userId);

      // ‚úÖ T·∫°o m·ªõi n·∫øu ch∆∞a c√≥ user
      await base(TABLE_NAME).create([
        {
          fields: {
            ZaloUID: userId,
            // ThreadID: threadId,
            event_name: event_name,
            LastInteraction: todayISOString,
            // C√≥ th·ªÉ th√™m c√°c tr∆∞·ªùng kh√°c nh∆∞ Name, Avatar, v.v. n·∫øu c√≥
          },
        },
      ]);

      console.log("‚úÖ ƒê√£ t·∫°o m·ªõi user trong Airtable:", userId);
      return;
    }

    const record = records[0];
    const oldDate = record.fields.LastInteraction;
    const today = todayISOString.slice(0, 10); // yyyy-mm-dd

    if (oldDate) {
      const lastDate = new Date(oldDate).toISOString().slice(0, 10);
      if (lastDate === today) {
        console.log("üü° C√πng ng√†y, kh√¥ng c·∫ßn update LastInteraction:", userId);
        return; // ‚ùå Kh√¥ng update
      }
    }

    // ‚úÖ Kh√°c ng√†y ‚Üí update LastInteraction
    await base(TABLE_NAME).update([
      {
        id: record.id,
        fields: {
          LastInteraction: todayISOString,
          last_event: event_name,
        },
      },
    ]);

    console.log("‚úÖ ƒê√£ update LastInteraction m·ªõi cho:", userId);
  } catch (err) {
    console.error("üî• L·ªói khi x·ª≠ l√Ω LastInteraction:", err);
  }
}

